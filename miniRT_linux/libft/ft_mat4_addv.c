#include <ft_mat4.h>

t_mat4
	m4_addv(t_mat4 m, t_vec3d v)
{
	m.inner[3][0] += v.x;
	m.inner[3][1] += v.y;
	m.inner[3][2] += v.z;
	return (m);
}
