#include "ft_vec3d.h"

t_vec3d
	v3d_cross(t_vec3d a, t_vec3d b)
{
	return (v3d_fullcon(
				a.y * b.z - a.z * b.y,
				a.z * b.x - a.x * b.z,
				a.x * b.y - a.y * b.x));
}
